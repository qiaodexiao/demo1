package com.at.ct.web.bean;

public class User {

    private Integer id;
    private String tel;
    private  String tname;

    public Integer getId() {
        return id;
    }

    public String getTel() {
        return tel;
    }

    public String getTname() {
        return tname;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }
}
